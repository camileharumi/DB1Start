package br.com.db1;

public class ExeciciosInteger {
	
	public Double exercicio1(Double valor1, Double valor2) {
		return valor1 + valor2;
	}
	
	public Double exercicio2(Double valor1, Double valor2) {
		return valor1 - valor2;
	}
	
	public Double exercicio3(Double valor1, Double valor2) {
		return valor1*valor2;
	}
	
	public Double exercicio4(Double valor1, Double valor2) {
		return valor1/valor2;
	}	
	
}
